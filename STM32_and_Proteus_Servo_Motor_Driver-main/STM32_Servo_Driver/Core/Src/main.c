void set_servo1(uint16_t position) {
  servo1_pos = position;
  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, position);
}

uint8_t read_button1(void) {
  return !HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1); // Pull-up olduğu için ters çevirdik
}

uint8_t read_button2(void) {
  return !HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2); // Pull-up olduğu için ters çevirdik
}

uint8_t read_button3(void) {
  return !HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_3); // Pull-up olduğu için ters çevirdik
}

void handle_button_control(void) {
  if (read_button1()) {
    // 1. buton basılı - PA1
    set_servo1(900);
  } else if (read_button2()) {
    // 2. buton basılı - PA2
    set_servo1(550);
  } else if (read_button3()) {
    // 3. buton basılı - PA3
    set_servo1(100);
  } else {
    // Hiçbir buton basılı değil - varsayılan pozisyon
    set_servo1(1500);  // Orta pozisyon
  }
}
